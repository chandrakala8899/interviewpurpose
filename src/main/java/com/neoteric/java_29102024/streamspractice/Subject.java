package com.neoteric.java_29102024.streamspractice;

public class Subject {
    private String name;
    private int marks;

    // Constructor
    public Subject(String name, int marks) {
        this.name = name;
        this.marks = marks;
    }

    // Getters
    public String getName() {
        return name;
    }

    public int getMarks() {
        return marks;
    }
}
